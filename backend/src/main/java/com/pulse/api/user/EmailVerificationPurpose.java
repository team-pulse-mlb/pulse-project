package com.pulse.api.user;

/**
 * 이메일 인증을 사용하는 목적을 구분합니다.
 *
 * 같은 이메일이라도 회원가입 인증과 비밀번호 변경 인증은
 * 서로 독립적으로 관리되어야 합니다.
 *
 * 예:
 * - 회원가입 인증을 마쳤다고 해서 비밀번호 변경을 허용하면 안 됩니다.
 * - 비밀번호 변경 인증을 마쳤다고 해서 회원가입 인증으로 재사용하면 안 됩니다.
 */
public enum EmailVerificationPurpose {

    /**
     * 신규 회원가입을 위한 이메일 인증입니다.
     */
    SIGNUP,

    /**
     * 로그인한 사용자의 비밀번호 변경을 위한 이메일 재인증입니다.
     */
    PASSWORD_CHANGE
}